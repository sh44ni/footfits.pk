'use client';

import { useState } from 'react';
import { MessageCircle, Send, X, Phone } from 'lucide-react';

export default function WhatsAppWidget() {
    const [isOpen, setIsOpen] = useState(false);
    const [message, setMessage] = useState('');

    const phoneNumber = '923132319987';

    const handleSend = (e: React.FormEvent) => {
        e.preventDefault();
        const text = encodeURIComponent(message || "Hi! I have a question about your products.");
        window.open(`https://wa.me/${phoneNumber}?text=${text}`, '_blank');
        setMessage('');
        setIsOpen(false);
    };

    return (
        <div className="fixed bottom-24 md:bottom-6 right-6 z-50 flex flex-col items-end">
            {/* Chat Window */}
            {isOpen && (
                <div className="mb-4 bg-white rounded-2xl shadow-xl w-80 overflow-hidden border border-gray-100 animate-in slide-in-from-bottom-5 fade-in duration-200 origin-bottom-right">
                    {/* Header */}
                    <div className="bg-[#128C7E] p-4 flex items-center justify-between text-white">
                        <div className="flex items-center gap-3">
                            <div className="bg-white/20 p-2 rounded-full">
                                <MessageCircle className="w-5 h-5 text-white" />
                            </div>
                            <div>
                                <h3 className="font-bold text-sm">FootFits Support</h3>
                                <p className="text-[10px] text-white/80">Typically replies instantly</p>
                            </div>
                        </div>
                        <button
                            onClick={() => setIsOpen(false)}
                            className="text-white/80 hover:text-white hover:bg-white/10 p-1 rounded-full transition-colors"
                        >
                            <X className="w-5 h-5" />
                        </button>
                    </div>

                    {/* Body */}
                    <div className="bg-[#E5DDD5] p-4 h-64 overflow-y-auto bg-opacity-50">
                        <div className="bg-white p-3 rounded-tr-xl rounded-br-xl rounded-bl-xl shadow-sm text-sm text-gray-800 max-w-[85%] mb-2">
                            ðŸ‘‹ Hi there! How can we help you today?
                        </div>
                    </div>

                    {/* Footer (Input) */}
                    <div className="p-3 bg-gray-50 border-t border-gray-100">
                        <form onSubmit={handleSend} className="flex gap-2">
                            <input
                                type="text"
                                value={message}
                                onChange={(e) => setMessage(e.target.value)}
                                placeholder="Type a message..."
                                className="flex-1 px-4 py-2 text-sm rounded-full border border-gray-200 focus:outline-none focus:border-[#128C7E] focus:ring-1 focus:ring-[#128C7E]"
                                autoFocus
                            />
                            <button
                                type="submit"
                                className="bg-[#128C7E] text-white p-2 rounded-full hover:bg-[#075E54] transition-colors shadow-sm flex-shrink-0"
                            >
                                <Send className="w-4 h-4" />
                            </button>
                        </form>
                    </div>
                </div>
            )}

            {/* Toggle Button */}
            <button
                onClick={() => setIsOpen(!isOpen)}
                className={`transition-all duration-300 shadow-lg hover:shadow-xl rounded-full flex items-center justify-center ${isOpen ? 'bg-gray-600 rotate-90 scale-90 w-12 h-12' : 'bg-[#25D366] hover:bg-[#20b85c] hover:scale-110 w-14 h-14 animate-bounce-slow'}`}
                aria-label={isOpen ? "Close Chat" : "Open WhatsApp Chat"}
            >
                {isOpen ? (
                    <X className="w-6 h-6 text-white" />
                ) : (
                    // WhatsApp Logo SVG
                    <svg viewBox="0 0 24 24" className="w-8 h-8 fill-white" xmlns="http://www.w3.org/2000/svg">
                        <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                    </svg>
                )}
            </button>
        </div>
    );
}
